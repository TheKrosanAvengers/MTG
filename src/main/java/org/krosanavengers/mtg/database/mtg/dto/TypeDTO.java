package org.krosanavengers.mtg.database.mtg.dto;

public class TypeDTO {
    int id;
    String name;

    public TypeDTO(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
